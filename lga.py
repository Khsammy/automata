# Get the lga and ward number


def get_lga_number(lga_name):
    if len(str(lga_name)) == 0:
        raise SystemExit('LGA => Empty lga')
    else:
        return list_of_lga[str(lga_name).upper()]['value']


def get_ward_number(lga_name, ward_name):
    if len(str(lga_name)) == 0:
        raise SystemExit('WARD => Empty lga')
    elif str(ward_name) == '':
        return ''
    else:
        return list_of_lga[str(lga_name).upper()]['ward'][str(ward_name).upper()]


